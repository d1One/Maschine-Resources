local class = require 'Scripts/Shared/Helpers/classy'
SceneBackground = class( 'SceneBackground' )

------------------------------------------------------------------------------------------------------------------------
-- init
------------------------------------------------------------------------------------------------------------------------

function SceneBackground:__init()
end

------------------------------------------------------------------------------------------------------------------------

function SceneBackground:setup(Screen)

    self.Widget = NI.GUI.insertSceneBGWidget(Screen, "SceneBG")
    self.Widget:style("")

    self.Widget:setVisible(false)
    self.Widget:setNoAlign(true)

end

------------------------------------------------------------------------------------------------------------------------

function SceneBackground:setActive(Active)

    self.Widget:setActive(Active)

end

